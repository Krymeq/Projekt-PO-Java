    private void delDistribActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delDistribActionPerformed
        DefaultListModel model = (DefaultListModel)distribList.getModel();
        /*
        Usuwanie produktów dystrybutora, który został wybrany do usunięcia
        */
        LinkedList<Product> toDelete = new LinkedList();
        if (distribList.getSelectedValue() != null)
        {
            for (Product i : symulacja.getListaProduktow())
            {

                /*
                 *  Jeśli dystrybutorem danego produktu jest dystrybutor przeznaczony do
                 *  usunięcia, to dodaj go do listy produktów do usunięcia.
                 */
                if (i.getDystr() == distribList.getSelectedValue().getByt())
                {
                    toDelete.add(i);
                }
            }

            DefaultListModel productModel = (DefaultListModel)productList.getModel();
            for (Product i : toDelete)
            {
                productModel.removeElement(i);
            }

            symulacja.getListaProduktow().removeAll(toDelete);

            distribList.getSelectedValue().setCzySkonczyc(true);
            symulacja.getListaDystrybutorow().remove(distribList.getSelectedValue());
            model.removeElement(distribList.getSelectedValue());
        }
    }//GEN-LAST:event_delDistribActionPerformed

