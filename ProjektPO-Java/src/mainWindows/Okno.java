    private void updateGUI()
    {
        if (this.symulacja.isFinish())
            resetSimulation();
        /*
            Odświeżanie wskazania daty i salda:
        */
        double a = symulacja.getFlixnet().getSaldo();
        double x = Math.round(a * 100.0) / 100.0;
        saldoLabel.setText("Saldo: " + Double.toString(x));
        timeLabel.setText(
                "Data: " +
                new SimpleDateFormat("dd.MM.yyyy").format(
                symulacja.getObecnaData().getTime())
                );
        
        /*
        Update listy produktów: przeiteruj przez całą listę i dodaj wszystkie, które
        się powtarzają
        */
        for (int i = 0; i < symulacja.getListaProduktow().size(); i++)
        {
            // Jeśli model nie zawiera danego produktu z listy
            if (!((DefaultListModel)productList.getModel()).
                    contains(symulacja.getListaProduktow().get(i)))
            {   // to go dodaj do niego.
                ((DefaultListModel)productList.getModel()).addElement
                                (symulacja.getListaProduktow().get(i));
            }
        }
        // Drugi etap: stwórz listę indeksów elementów, które zawiera model listy
        // a których nie zawierają właściwe dane. Potrzebujemy wykonywać ten krokjedynie
        // dla produktów, ponieważ jedynie one mogą być usunięte w sytuacji innej niż
        // na przycisk.
        
        LinkedList<Product> toDelete = new LinkedList();
        DefaultListModel<Product> dlmProd = (DefaultListModel)productList.getModel();
        for (int i = 0; i < productList.getModel().getSize(); i++)
        {
            if (!symulacja.getListaProduktow().contains(dlmProd.get(i)))
                toDelete.add(dlmProd.get(i));
        }
        
        for (Product i : toDelete)
        {
            dlmProd.removeElement(i);
        }
        
        /*
         * Update listy klientów:
         */
        for (int i = 0; i < symulacja.getListaKlientow().size(); i++)
        {
            // Jeśli model nie zawiera danego klientu z listy
            if (!((DefaultListModel)clientList.getModel()).
                    contains(symulacja.getListaKlientow().get(i)))
            {   // to go dodaj do niego.
                ((DefaultListModel)clientList.getModel()).addElement
                                (symulacja.getListaKlientow().get(i));
            }
        }
        
        /*
         * Update listy dystrybutorów
         */
        for (int i = 0; i < symulacja.getListaDystrybutorow().size(); i++)
        {
            // Jeśli model nie zawiera danego klientu z listy
            if (!((DefaultListModel)distribList.getModel()).
                    contains(symulacja.getListaDystrybutorow().get(i)))
            {   // to go dodaj do niego.
                ((DefaultListModel)distribList.getModel()).addElement
                                (symulacja.getListaDystrybutorow().get(i));
            }
        }
    }
