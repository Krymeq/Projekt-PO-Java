    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultListModel<Product> dlm = new DefaultListModel();
        String str = filterField.getText();
        if (!str.equals(""))
        {
            for (Product i: symulacja.getListaProduktow())
            {
                if (nameRadioBtn.isSelected())
                {
                    if (i.toString().contains(str))
                    {
                        dlm.addElement(i);
                    }
                }

                else if (actRadioBtn.isSelected())
                {
                    if (i instanceof Film)
                    {
                        for (String s : ((Film) i).getListaAktorow())
                        {
                            if (s.contains(str))
                            {
                                dlm.addElement(i);
                                break;
                            }
                        }
                    }

                    else if (i instanceof Serial)
                    {
                        for (String s : ((Serial) i).getListaAktorow())
                        {
                            if (s.contains(str))
                            {
                                dlm.addElement(i);
                                break;
                            }
                        }
                    }
                }
            }
            
            FilterWindow wnd = new FilterWindow(dlm);
            wnd.setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

