    private void basicSubBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_basicSubBtnActionPerformed
        try
        {
            double cena = Math.round(Double.parseDouble(basicSubTextField.getText())*100) / 100;
            if (cena <= 0)
                throw new NumberFormatException();
            Subscription.setBasicPrice(cena);
            
            for (ExtThread klient : symulacja.getListaKlientow())
            {
                Klient kl = (Klient)klient.getByt();
                if (kl.getSub().getType().equals("Basic"))
                   kl.getSub().setPrice(cena);
            }
        }
        catch(NumberFormatException e)
        {    
            System.out.println("Podano złą wartość. Podana liczba musi być większa od zera.");
        }
    }//GEN-LAST:event_basicSubBtnActionPerformed

